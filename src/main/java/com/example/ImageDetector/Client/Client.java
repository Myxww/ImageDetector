package com.example.ImageDetector.Client;

import java.util.Date;

public class Client {
	private String name;
	private String email;
	private String pass;
	private Boolean gender;
	private Date dob;
	
	public String getName() {
		return name;
		
	}
	
	public String getEmail() {
		return email;
		
	}
	
	public String getPass() {
		return pass;
		
	}
	
	public Boolean getGender() {
		return gender;
		
	}
	
	public Date getDob() {
		return dob;
		
	}
	
	
	

	

}
